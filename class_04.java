//for while loop
public class class_04 {
    public static void main(String[] args) {

        int temp = 3000;
        // for(initialisation;condition;updation)
        for (int i = 0; i <= temp; i++) {
            System.out.println("I Love You " + i);
        }

        int i = 0;
        while (i < 11) {
            System.out.print(i + " ");
            i = i + 1;
        }
    }
}
